for i in range(10):
    print(i)
print("***************")

for i in range(3, 10):
    print(i)
print("***************")

for i in range(1,10,3): # 3 is jumping value, 1 is start value, 10 is end value
    print(i)
print("***************")

for i in range(10,0,-1): # -1 is jumping value , 10 is start value, 0 is start value
    print(i)
print("***************")
